#include <iostream>
using namespace std;

int main()
{
    short int A, B;
    cout << "please enter the first Number A?\n";
    cin >> A;
    cout << "please enter the second Number B?\n";
    cin >> B;
    cout << endl;
    cout << A << " + " << B << " = " << A + B << endl;
    cout << A << " - " << B << " = " << A - B << endl;
    cout << A << " * " << B << " = " << A * B << endl;
    cout << A << " / " << B << " = " << A / B << endl;
    cout << A << " % " << B << " = " << A % B << endl;
    return 0;
}
